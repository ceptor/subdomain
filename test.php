                                        <h3 class="h4 tPadding0 bPadding0 summary">
                                            <span class="expand_section">First Public Working Draft: Web Payments Use Cases 1.0</span>
                                        </h3>
                                        <p class="date">
                                            <span class="dtstart published" title="2015-04-16T15:31:25Z"> 16 April 2015 </span> | <a title="Archive: First Public Working Draft: Web Payments Use Cases 1.0" href="http://www.w3.org/blog/news/archives/4616?pk_campaign=feed&amp;pk_kwd=first-public-working-draft-web-payments-use-cases-1-0">Archive</a>
                                        </p>
                                    </div>
                                    <div class="description expand_description"><p>Today the <a href="/Payments/IG">Web Payments Interest Group</a> published a first draft of
                                            <a href="/TR/2015/WD-web-payments-use-cases-20150416/">Web Payments Use Cases</a>. The current payment landscape is changing rapidly, offering new opportunities for greater security, improved usability on mobile, and payment innovations in areas like digital wallets and cryptocurrencies. The lack of open standards for integrating the latest developments into the Web makes it more difficult for merchants, Web developers, and payment service providers to adapt to new payment solutions. Fragmented regulatory environments further complicate the payments landscape.</p>
                                        <p>To achieve greater interoperability among merchants and their customers, payment providers,
                                            software vendors, mobile operators, and payment networks, the W3C Web Payments Interest Group,
                                            <a href="http://www.w3.org/2014/10/payments.html.en">launched in October 2014</a>, is developing
                                            a roadmap for standards to improve the interoperability of payments on the Web. Today&#x2019;s use cases
                                            document establishes the group&#x2019;s initial scope of work. Guided by these use cases, the W3C
                                            Web Payments Interest Group (see the <a href="https://www.w3.org/2004/01/pp-impl/73816/status">current participants</a>) plans to derive architecture and associated technology requirements. That work will form the basis of conversations with W3C groups and the broader payments industry about what standards (from W3C or other organizations) will be necessary to fulfill the use cases and achieve the full potential of Web payments.</p>
                                        <p>Learn more about W3C&#x2019;s work on <a href="/Payments/">Payments on the Web</a>,
                                            supported in part by the European Union through the <a href="http://html5apps-project.eu/">HTML5Apps project</a>.</p>
