                                        <h3 class="h4 tPadding0 bPadding0 summary">
                                            <span class="expand_section">Four Drafts Published by the CSV on the Web Working Group</span>
                                        </h3>
                                        <p class="date">
                                            <span class="dtstart published" title="2015-04-16T18:03:59Z"> 16 April 2015 </span> | <a title="Archive: Four Drafts Published by the CSV on the Web Working Group" href="http://www.w3.org/blog/news/archives/4619?pk_campaign=feed&amp;pk_kwd=four-drafts-published-by-the-csv-on-the-web-working-group">Archive</a>
                                        </p>
                                    </div>
                                    <div class="description expand_description"><p>The <a href="http://www.w3.org/2013/csvw/wiki/Main_Page">CSV on the Web Working Group</a> has published a new set of Working Drafts, which the group considers feature complete and implementable.</p>
                                        <ul>
                                            <li><a href="http://www.w3.org/TR/2015/WD-tabular-data-model-20150416/">Model for Tabular Data and Metadata on the Web</a>: an abstract model for tabular data, and how to locate metadata that enables users to better understand what the data holds; this specification also contains non-normative guidance on how to parse CSV files</li>
                                            <li><a href="http://www.w3.org/TR/2015/WD-tabular-metadata-20150416/">Metadata Vocabulary for Tabular Data</a>: a JSON-based format for expressing metadata about tabular data to inform validation, conversion, display and data entry for tabular data</li>
                                            <li><a href="http://www.w3.org/TR/2015/WD-csv2json-20150416/">Generating JSON from Tabular Data on the Web</a>: how to convert tabular data into JSON</li>
                                            <li><a href="http://www.w3.org/TR/2015/WD-csv2rdf-20150416/">Generating RDF from Tabular Data on the Web</a>: how to convert tabular data into RDF</li>
                                        </ul>
                                        <p>The group is keen to receive comments on these specifications, either as <a href="https://github.com/w3c/csvw/issues">issues on the Group&#x2019;s GitHub repository</a> or by posting to <a href="mailto:public-csv-wg-comments@w3.org">public-csv-wg-comments@w3.org</a>.</p>
                                        <p>The CSV on the Web Working Group would also like to invite people to start implementing these specifications and to donate their test cases into the group&#x2019;s <a href="https://github.com/w3c/csvw/tree/gh-pages/tests">test suite</a>. Building this test suite, as well as responding to comments, will be the group&#x2019;s focus over the next couple of months.</p>
                                        <p>Learn more about the <a href="http://www.w3.org/2013/csvw/">CSV on the Web Working Group</a>.</p>
