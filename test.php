                                            <span class="expand_section">Clipboard API and events; File API Drafts Published</span>
                                        </h3>
                                        <p class="date">
                                            <span class="dtstart published" title="2015-04-21T15:49:09Z"> 21 April 2015 </span> | <a title="Archive: Clipboard API and events; File API Drafts Published" href="http://www.w3.org/blog/news/archives/4639?pk_campaign=feed&amp;pk_kwd=clipboard-api-and-events-file-api-drafts-published">Archive</a>
