                                    <div class="description expand_description"><p>The <a href="http://www.w3.org/Style/CSS/members">Cascading Style Sheets (CSS) Working Group</a> has published a Working Draft of <a href="http://www.w3.org/TR/2015/WD-css-cascade-4-20150421/">CSS Cascading and Inheritance Level 4</a>. CSS is a language for describing the rendering of structured documents (such as HTML and XML) on screen, on paper, in speech, etc. This CSS module describes how to collate style rules and assign values to all properties on all elements. By way of cascading and inheritance, values are propagated for all properties on all elements. Learn more about the <a href="http://www.w3.org/Style/">Style Activity</a>.</p>
