                                            <span class="expand_section">Requirements for Hangul Text Layout and Typography Draft Published</span>
                                        </h3>
                                        <p class="date">
                                            <span class="dtstart published" title="2015-04-14T18:24:40Z"> 14 April 2015 </span> | <a title="Archive: Requirements for Hangul Text Layout and Typography Draft Published" href="http://www.w3.org/blog/news/archives/4603?pk_campaign=feed&amp;pk_kwd=requirements-for-hangul-text-layout-and-typography-draft-published">Archive</a>
